       Trung tâm Chiếu phim Quốc Gia (National Cinema Center) là đơn vị sự nghiệp văn hóa có thu, trực thuộc Bộ Văn hóa, Thể thao và Du lịch, được thành lập vào ngày 29/12/1997. Trung tâm có chức năng tổ chức chiếu phim phục vụ các nhiệm vụ chính trị, xã hội, hợp tác quốc tế; điều tra xã hội học về nhu cầu khán giả để phục vụ cho công tác định hướng phát triển ngành Điện ảnh; trưng bày điện ảnh và tổ chức các hoạt động biểu diễn nghệ thuật.

  Nằm tại vị trí trung tâm trên 2 mặt phố chính là Phố Láng Hạ và Thái Hà, tòa nhà Trung tâm Chiếu phim Quốc gia được thiết kế hiện đại, khu để xe rộng rãi, giao thông thuận tiện. Đây là một địa chỉ quen thuộc và yêu mến đối với những người yêu điện ảnh Thủ đô và cả nước.

	Trung tâm Chiếu phim Quốc Gia được thiết kế đa năng với hệ thống rạp chiếu phim hiện đại gồm 10 phòng chiếu, trình chiếu được tất cả các thể loại phim: 35mm, 2D, 3D và 4D cùng với tổ hợp Khu trưng bày điện ảnh – Biểu diễn nghệ thuật – Tổ chức sự kiện và các dịch vụ giải trí để phục vụ khán giả Thủ đô.
      
   Các phòng chiếu phim của Trung tâm có tổng cộng 1.522 chỗ ngồi với hơn 60 suất chiếu/ ngày. Phòng lớn nhất 402 chỗ, phòng nhỏ nhất 85 chỗ.

